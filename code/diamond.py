n=int(input())
i=1
l=1
n1=(n+1)//2
while(i<=n1):
    s=1
    while(s<=n1-i):
        print(' ',end='')
        s+=1
    j=1
    while(j<=i):
        print("*",end="")
        l+=1
        j+=1
    k=2*i-1
    while(k>=i):
        print("*",end="")
        k-=1
    print()
    i+=1
n2=n//2;
i=n2
while(i>=1):
    s=1
    while(s<=n2-i+1):
        print(' ',end='')
        s+=1
    j=1
    while(j<=i):
        print("*",end="")
        l+=1
        j+=1
    k=2*i-1
    while(k>=i):
        print("*",end="")
        k-=1
    print()
    i-=1
